function pickRandom(obj) {
    var result;
    var count = 0;
    for (var prop in obj)
        if (Math.random() < 1/++count)
           result = prop;
    return result;
}

var last = null;
var next = null;
var history = null;
var points = null;

function style_left() {
    document.getElementById("left-name").innerText = `“${data[last].name}”`;
    document.getElementById("left-subs").innerText = (data[last].subs).toLocaleString();
}
function style_right() {
    document.getElementById("right-name").innerText = `“${data[next].name}”`;
    document.getElementById("right-compare").innerText = `subscribers than “${data[last].name}”`;
}

function button_click(val) {
    if (val) { // user says More
        if (data[next].subs >= data[last].subs) { // true
            victory();
        }
        else {
            loss();
        }
    }
    else { // user says Less
        if (data[next].subs <= data[last].subs) { // true
            victory();
        }
        else {
            loss();
        }
    }
}
function victory() {
    points++;
    last = next;
    next = pickRandom(data);
    style_left();
    style_right();
}
function loss() {
    document.getElementById("loss-points").innerText = `${points} points`
    document.getElementById("loss-screen").classList.remove("hidden");
}

function new_game() {
    document.getElementById("loss-screen").classList.add("hidden");
    last = pickRandom(data);
    next = pickRandom(data);
    history = [];
    points = 0;
    style_left();
    style_right();
}

new_game();